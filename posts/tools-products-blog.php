    <p class=MsoNormal><span lang=EN-US>Starting to pick interest in makeup? Do you
    love and appreciate that great and flawless look you see on celebrities and
    people when they use makeup? Have you tried learning to do makeup and you seem
    to be missing that all-important touch? Well I want to assure you that it is
    possible for you, yes <b>you</b>, to get that exquisite touch you're looking
    for. And, it's not rocket science, it's not even difficult. To accomplish that
    look you're looking for, it is important, in fact, <b>imperative</b>, that you
    have the right tools. </span></p>

    <p class=MsoNormal><span lang=EN-US>It is not only possible, but very common to
    find that individuals use the wrong <b>tools</b> for applying their <b>products</b>.
    While each makeup tool is designed for a specific purpose, at times it could be
    a little intimidating, especially to those just starting, to identify the right
    use and the right way since a single tool could serve multiple purposes, and
    some may even be used in the absence of another. </span></p>

    <p class=MsoNormal><span lang=EN-US>How then can <b>you </b>know the right tool
    to use? Come along with me, and let's learn about <b>five tools </b><i>and </i><b>five
    products</b> you <b>must</b> have as a makeup beginner.</span></p>

    <h5><span lang=EN-US>TOOLS</span></h5>

    <hr />

    <h1><span lang=EN-US>Powder Brush</span></h1>

    <p class=MsoNormal><img style="width: 98px; height: 92px; float: left;"
    src="Honey%20Highlights%20Blog%20Posts%20(1)_files/powder-brush.jpg" alt="powder brush.jpg" 
    v:shapes="Picture_x0020_10"><span lang=EN-US>A powder brush is usually 
      a fluffy and round shaped brush designed in such a way that blends powder 
      makeup as you apply it, such that some refer to it as a big blending brush. 
      There are some angled or slant shaped brushes, and these are better used for 
      applying blush and bronze, as well as, for highlighting specific parts of the face.
    </span></p>

    <p class=MsoNormal><span lang=EN-US>To use: </span></p>

    <p class=MsoNormal><span lang=EN-US>Swirl the brush on top of the powder, tap
    it off to get rid of excess powder, and then apply it from the center of the
    face to the outer part of the face.</span></p>

    <hr />

    <h1><span lang=EN-US>Foundation brush/beauty sponge</span></h1>

    <p class=MsoNormal><img style="width: 100px; height: 100px; float: left; margin-right: 5px;"
    src="Honey%20Highlights%20Blog%20Posts%20(1)_files/beauty-sponge.jpg" 
    v:shapes="Picture_x0020_1"><span lang=EN-US>It is a round, domed shaped 
      densely packed brush which allows blending of <b style='mso-bidi-font-weight:normal'>foundation</b>
      as you brush. When using this brush don't be too heavy handed instead 
      build up the amount of product you would like to apply instead of brushing 
      all at once. A sponge is ideal for a flawless and natural foundation application; 
      it is also very easy to use.
    </span></p>

    <p class=MsoNormal><span lang=EN-US>How to use:</span></p>

    <p class=MsoNormal><span lang=EN-US>Start from the center of the face and work to the outward corner in
    circular motions, for a beauty sponge make sure its damp before you use.</span></p>

    <hr />

    <h1><span lang=EN-US>Blending Brush</span></h1>

    <p class=MsoNormal><img style="width: 98px; height: 98px; float: left;"
    src="Honey%20Highlights%20Blog%20Posts%20(1)_files/blending-brush.jpg" 
    v:shapes="Picture_x0020_9"><span lang=EN-US>A blending brush is a loose 
      dome shaped brush perfect for blending both wet and dry products. 
      It can be used for applying eye shadow which blends colors effortlessly 
      on the eye lid and crease. It can also be used to <b style='mso-bidi-font-weight:normal'>blend</b>
      concealer to the skin; you can have as many blending brushes as you want 
      depending on the purpose you want it to serve.
    </span></p>

    <p class=MsoNormal><span lang=EN-US>How to use:</span></p>

    <p class=MsoNormal><span lang=EN-US>In sweeping movement, glide brush across lid to apply eye color.</span></p>

    <hr />

    <h1><span lang=EN-US>Eyebrow Brush</span></h1>

    <p class=MsoNormal><img style="width: 92px; height: 108px; float: left;"
    src="Honey%20Highlights%20Blog%20Posts%20(1)_files/eyebrow-brush.jpg" 
    hspace=12 v:shapes="_x0000_s1038"><span lang=EN-US>This brush is
    commonly referred to as an angled brush, it is slanted linear brush used to
    both <b style='mso-bidi-font-weight:normal'>fill </b>and <b style='mso-bidi-font-weight:
    normal'>highlight</b> brows. This brush helps to create precise lines and
    noticeable definition while applying your concealer.</span></p>

    <p class=MsoNormal><span lang=EN-US>How to use:</span></p>

    <p class=MsoNormal><span lang=EN-US>There are techniques and method on how to
    use this brush, to get a detailed explanation tap the link below.</span></p>

    <p class=MsoNormal><span lang=EN-US>Find an easy eyebrow tutorial video here: <a
    href="https://youtu.be/wX5TtjoNBvs">https://youtu.be/wX5TtjoNBvs</a></span></p>

    <hr />

    <h1><span lang=EN-US>Spoolie / Mascara Wand</span></h1>

    <p class=MsoNormal><img style="width:106; height:106; float:left" width=106 height=106 
    src="Honey%20Highlights%20Blog%20Posts%20(1)_files/spoolie.jpg" 
    hspace=12 v:shapes="Picture_x0020_11"><span lang=EN-US>It is a long
    brush with soft and spiral bristles. They are used for <b style='mso-bidi-font-weight:
    normal'>brushing</b> and <b style='mso-bidi-font-weight:normal'>blending</b>
    brow fillers. They can also be used to remove clumps following mascara
    application. </span></p>

    

