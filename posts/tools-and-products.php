                        <p>Starting to pick interest in makeup? Do you love and appreciate that great and flawless look 
                            you see on celebrities and people when they use makeup? Have you tried learning to do makeup 
                            and you seem to be missing that all-important touch? Well I want to assure you that it is 
                            possible for you, yes <b>you</b>, to get that exquisite touch you're looking for. And, it's 
                            not rocket science, it's not even difficult. To accomplish that look you're looking for, it 
                            is important, in fact, <b>imperative</b>, that you have the right tools. 
                        </p>
                        <p>It is not only possible, but very common to find that individuals use the wrong <b>tools</b> 
                            for applying their <b>products</b>. While each makeup tool is designed for a specific purpose, 
                            at times it could be a little intimidating, especially to those just starting, to identify the 
                            rightuse and the right way since a single tool could serve multiple purposes, and some may even 
                            be used in the absence of another.
                        </p>
                        <p>How then can <b>you </b>know the right tool to use? Come along with me, and let's learn about 
                            <b>five tools </b><i>and </i><b>five products</b> you <b>must</b> have as a makeup beginner.
                        </p>
                        <div class="section-heading"><p class="heading-five">TOOLS</p></div>
                        <div><hr/></div>
                        <div class="container" id="powder-brush">
                            <p class="heading-one" style="color: #c56765;">Powder Brush</p>
                            <p><img style="width: 98px; height: 92px; float: left;"
                                src="Honey%20Highlights%20Blog%20Posts%20(1)_files/powder-brush.jpg" alt="powder brush.jpg" />
                                A powder brush is usually a fluffy and round shaped brush designed in such a way that blends 
                                powder makeup as you apply it, such that some refer to it as a big blending brush. There are 
                                some angled or slant shaped brushes, and these are better used for applying blush and bronze, 
                                as well as, for highlighting specific parts of the face.
                            </p>
                            <p>To use:</p>
                            <p>Swirl the brush on top of the powder, tap it off to get rid of excess powder, and then apply it 
                                from the center of the face to the outer part of the face.
                            </p>
                        </div>
                        <div><hr/></div>
                        <div class="container" id="foundation-brush">
                            <p class="heading-one" style="color: #c56765;">Foundation Brush/Beauty Sponge</p>
                            <p><img src="Honey%20Highlights%20Blog%20Posts%20(1)_files/beauty-sponge.jpg" alt="foundation brush"
                                style="width: 100px; height: 100px; float: left; margin-right: 5px;" />
                                It is a round, domed shaped densely packed brush which allows blending of 
                                <b>foundation</b> as you brush. When using this brush don't be too heavy handed instead build 
                                up the amount of product you would like to apply instead of brushing all at once. A sponge is 
                                ideal for a flawless and natural foundation application; it is also very easy to use.
                            </p>
                            <p>To use:</p>
                            <p>Start from the center of the face and work to the outward corner in circular motions, for 
                                a beauty sponge make sure its damp before you use.
                            </p>
                        </div>
                        <div><hr/></div>
                        <div class="container" id="blending-brush">
                            <p class="heading-one" style="color: #c56765;">Blending Brush</p>
                            <p><img style="width: 98px; height: 98px; float: left;"
                                src="Honey%20Highlights%20Blog%20Posts%20(1)_files/blending-brush.jpg" alt="blending brush.jpg" />
                                A blending brush is a loose dome shaped brush perfect for blending both wet and dry products.
                                It can be used for applying eye shadow which blends colors effortlessly on the eye lid and 
                                crease. It can also be used to <b>blend</b> concealer to the skin; you can have as many blending 
                                brushes as you want depending on the purpose you want it to serve.
                            </p>
                            <p>To use:</p>
                            <p>In sweeping movement, glide brush across lid to apply eye color.</p>
                        </div>
                        <div><hr/></div>
                        <div class="container" id="eyebrow-brush">
                            <p class="heading-one" style="color: #c56765;">Eyebrow Brush</p>
                            <p><img style="width: 92px; height: 108px; float: left;"
                                src="Honey%20Highlights%20Blog%20Posts%20(1)_files/eyebrow-brush.jpg" alt="eyebrow brush.jpg" />
                                This brush is commonly referred to as an angled brush, it is slanted linear brush used to 
                                both <b>fill </b>and <b>highlight</b> brows. This brush helps to create precise lines and noticeable 
                                definition while applying your concealer.
                            </p>
                            <p>To use:</p>
                            <p>There are techniques and method on how to use this brush, to get a detailed explanation tap the link below.</p>
                            <p>Find an easy eyebrow tutorial video here: <a href="https://youtu.be/wX5TtjoNBvs">https://youtu.be/wX5TtjoNBvs</a></p>
                        </div>
                        <div><hr/></div>
                        <div class="container" id="spoolie">
                            <p class="heading-one" style="color: #c56765;">Spoolie/Mascara Wand</p>
                            <p><img style="width: 96px; height: 96px; float: left;"
                                src="Honey%20Highlights%20Blog%20Posts%20(1)_files/spoolie.jpg" alt="spoolie.jpg" />
                                It is a long brush with soft and spiral bristles. They are used for 
                                <b>brushing</b> and <b>blending</b> brow fillers. They can also be used to remove clumps 
                                following mascara application.
                            </p>
                        </div>
                        <div><hr/></div>
                        <div class="container">
                            <p>Now that you know these basic tools you might be wondering what kind of products can they be 
                                used for and which is most important, all these questions will be answered below as I explain 
                                five products that you should have as a makeup beginner.
                            </p>
                        </div>
                        <div class="section-heading"><p class="heading-five">PRODUCTS</p></div>
                        <div><hr/></div>
                        <div class="container" id="foundation">
                            <p class="heading-one" style="color: #c56765;">Foundation</p>
                            <p><img style="width: 124px; height: 62px; float: left; padding-right: 5px;"
                                src="Honey%20Highlights%20Blog%20Posts%20(1)_files/foundation.jpg" alt="foundation.jpg" />
                                A foundation is a liquid applied to the face to create an even, uniform color to the 
                                complexion. It covers flaws and gives the desired finish <b>matte </b>or dewy 
                                look depending on what you want. Foundations work best when matched with the 
                                <b>skin tone.</b>
                            </p>
                        </div>
                        <div><hr/></div>
                        <div class="container" id="concealer">
                            <p class="heading-one" style="color: #c56765;">Concealer</p>
                            <p>Concealers could also be known as a color corrector, it is a type of cosmetics used to mask 
                                dark circles, age spots, large pores and other blemish visible to the skin, it can also be 
                                used to <b>highlight</b> T zone areas on your face and also <span class=GramE>you</span> brow 
                                areas. It covers area that you find imperfect, however it cannot act alone. 
                            </p>
                        </div>
                        <div><hr/></div>
                        <div class="container" id="powder">
                            <p class="heading-one" style="color: #c56765;">A Powder</p>
                            <p><img style="width: 124px; height: 82px; float: left; margin-right: 10px;"
                                src="Honey%20Highlights%20Blog%20Posts%20(1)_files/powder.jpg" alt="powder.jpg" />
                                It could be compact or loose depending on your choice; it also works best when it's 
                                the <b>same shade</b> with your <b>skin tone</b> and is usually applied right after a foundation. 
                                It is important to know that to avoid looking cakey, use only one coat of powder on 
                                your face and avoid adding excess powder.
                            </p>
                        </div>
                        <div><hr/></div>
                        <div class="container" id="brow-pencil">
                            <p class="heading-one" style="color: #c56765;">A Brow Pencil / Brow Gel</p>
                            <p><img style="width: 124px; height: 83px; float: left; padding-right: 5px;"
                                src="Honey%20Highlights%20Blog%20Posts%20(1)_files/brow-pencil.jpg" alt="brow-pencil.jpg" />
                                This is used to <b>fill</b> the brows and make it look fuller and a little bit darker, a brow pencil 
                                should be used when your brows are more <b>defined</b> and full but a brow gel should be used if your 
                                brows are sparse and you need them to visible.
                            </p>
                        </div>
                        <div><hr/></div>
                        <div class="container" id="lipstick">
                            <p class="heading-one" style="color: #c56765;">A Lipstick / Lip Gloss</p>
                            <p><img style="width: 124px; height: 82px; float: left; padding-right: 5px;"
                                src="Honey%20Highlights%20Blog%20Posts%20(1)_files/lipstick.jpg" alt="lipstick.jpg" />
                                Depending on your choice and occasion, you could use a lip-gloss for a casual outing this 
                                prevents your lips from cracking and looking dry but a lipstick on the other hand could give 
                                you a bold look especially if you have an event or an occasion depending on your preference.
                            </p>
                        </div>
                        <div><hr/></div>
                        <div class="container">
